#TO INPUT CHOICE FROM USER AND PRINT CORRESPONDING DAY
CHOICE = int(input("Enter your choice(1-7):"))
if CHOICE == 1:
    print("MONDAY")
elif CHOICE == 2:
    print("TUESDAY")
elif CHOICE == 3:
    print("WEDNESDAY")
elif CHOICE == 4:
    print("THURSDAY")
elif CHOICE == 5:
    print("FRIDAY")
elif CHOICE == 6:
    print("SATURDAY")
elif CHOICE == 7:
    print("SUDAY")
else :
    print("Wrong choice entered")
